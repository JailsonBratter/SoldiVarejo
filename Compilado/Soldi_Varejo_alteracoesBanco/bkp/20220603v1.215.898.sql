				DROP TABLE #CONSOLIDAR

			SELECT 
			codigo = A.PLU,
			descricao = C.DESCRICAO, 
			quantidade = A.QTDE, 
			valor = A.unitario,
			C.Tipo,
			C.Peso_Variavel
			INTO #SEMCONSOLIDAR
			FROM COMANDA_ITEM AS A 
