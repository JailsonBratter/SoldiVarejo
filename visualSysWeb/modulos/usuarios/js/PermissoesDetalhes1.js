function selecionarProduto(elemento) {
    var Inputs = MainContent_TabContainer1_TabPanel2_gridProdutos.getElementsByTagName("input");
    for (var i = 0; i < Inputs.length; ++i) {
        if (Inputs[i].type == 'checkbox') {
            Inputs[i].checked = elemento.checked;

        }
    }
}

