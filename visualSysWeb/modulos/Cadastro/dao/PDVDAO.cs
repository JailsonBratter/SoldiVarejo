        public PDVDAO(int campoIndex, User usr)
        { //colocar campo index da tabela
            this.usr = usr;
            String sql = "Select * from  Controle_Filial_PDV where Controle_Filial_PDV.Filial = '" + usr.filial.Filial + "' AND Caixa =" + campoIndex.ToString();
            SqlDataReader rs = Conexao.consulta(sql, usr, true);
            carregarDados(rs);
        }

        private String dataBr(DateTime dt)
        {
            if (dt.ToString("dd/MM/yyyy").Equals("01/01/0001"))
            {
                return "";
            }
            else
            {
                return dt.ToString("dd/MM/yyyy");
            }
        }

        public void carregarDados(SqlDataReader rs)
        {
            try
            {
                if (rs.Read())
                {
                    PDV = int.Parse(rs["Caixa"].ToString());
                    Modelo = (rs["Modelo"].ToString().Equals("") ? "" : rs["Modelo"].ToString());
                    NumeroSerie = (rs["FabEqu"].ToString().Equals("") ? "" : rs["FabEqu"].ToString());
                    Diretorio_Carga = (rs["Diretorio_Carga"].ToString().Equals("") ? "" : rs["Diretorio_Carga"].ToString());
                    sat = rs["sat"].ToString().Equals("1");
                    Link_server = rs["Link_server"].ToString();
                    ativa_link_server = Funcoes.intTry(rs["ativa_link_server"].ToString());
                    DateTime.TryParse(rs["Data_Ult_atualizacao"].ToString(), out Data_ult_atualizacao);
                    Carga_Automatica = rs["Carga_Automatica"].ToString().Equals("1");
                    Integracao_Vendas_automatica = rs["Integracao_Vendas_Automatica"].ToString().Equals("1");
                    ConnectionString = rs["ConnectionString"].ToString();
                    PDVTerceiros = rs["PDV_Terceiros"].ToString();

                }
            }
            catch (Exception)
            {

                throw;
            }
            finally
            {
                if (rs != null)
                    rs.Close();
            }
        }
        private void update()
        {
            try
            {
                String sql = "UPDATE Controle_Filial_PDV SET " +
                              "Caixa = " + PDV.ToString().Replace(",", ".") +
                              ", Modelo = '" + Modelo + "'" +
                              ", FabEqu = '" + NumeroSerie + "'" +
                              ", Diretorio_Carga = '" + Diretorio_Carga + "'" +
                              ", sat =" + (sat ? "1" : "0") +
                              ",Link_server= '" + Link_server + "'" +
                              ",ativa_link_server=" + ativa_link_server +
                              ",data_ult_atualizacao =" + (Data_ult_atualizacao.ToString("dd-MM-yyyy").Equals("01-01-0001") ? "null" : Data_ult_atualizacao.ToString("dd-MM-yyyy"))+
                              ",carga_automatica="+(Carga_Automatica?"1":"0")+
                              ",Integracao_Vendas_automatica="+(Integracao_Vendas_automatica?"1":"0")+
                              ",ConnectionString='"+ConnectionString+"'"+
                              ",PDV_Terceiros='"+PDVTerceiros+"'"+
                    "  WHERE Filial = '" + usr.filial.Filial + "' AND Caixa = " + PDV;
                Conexao.executarSql(sql);
            }
            catch (Exception err)
            {
                throw new Exception("nao foi possivel Atualizar os valores erro:" + err.Message);
            }
        }
        public bool salvar(bool novo)
        {
            if (novo)
            {
                insert();
            }
            else
            {
                update();
            }
            return true;
        }

        public bool excluir()
        {
            String sql = "DELETE from Controle_Filial_PDV WHERE Controle_Filial_PDV.Filial = '" + usr.filial.Filial + "' AND Caixa = " + PDV.ToString(); //colocar campo index
            Conexao.executarSql(sql);
            return true;
        }

        private void insert()
        {
            try
            {
                String sql = " INSERT INTO Controle_Filial_PDV (" +
                              "Filial, " +
                              "Modelo, " +
                              "FabEqu, " +
                              "Caixa, " +
                              "Diretorio_Carga" +
                              ",sat" +
                              ",Link_Server"+
                              ",ativa_link_server"+
                              ",data_ult_atualizacao"+
                              ",Carga_automatica"+
                              ",Integracao_Vendas_automatica"+
                              ",ConnectionString"+
                              ",PDV_Terceiros"+
                          ") VALUES (" +
                            "'" + usr.filial.Filial + "'" +
                            ", '" + Modelo + "'" +
                            ", '" + NumeroSerie + "'" +
                            ", " + PDV.ToString() +
                            ", '" + Diretorio_Carga + "'" +
                            "," + (sat ? "1" : "0") +
                            ",'"+Link_server+"'"+
                            "," + ativa_link_server+
                            "," + (Data_ult_atualizacao.ToString("dd-MM-yyyy").Equals("01-01-0001") ? "null" : Data_ult_atualizacao.ToString("dd-MM-yyyy")) +
                            ","+(Carga_Automatica?"1":"0")+
                            ","+(Integracao_Vendas_automatica?"1":"0")+
                            ",'"+ConnectionString+"'"+
                            ",'"+PDVTerceiros+"'"+
                            ")";


                Conexao.executarSql(sql);
            }
            catch (Exception err)
            {
                throw new Exception("nao foi possivel Inserir os valores erro:" + err.Message);
