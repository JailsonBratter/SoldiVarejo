        public bool padrao = false;
        public TransportadoraDAO() { }
        public TransportadoraDAO(String nomeTransportadora, User usr)
        { //colocar campo index da tabela

            carregarDados(nomeTransportadora,usr);
        }

        private String dataBr(DateTime dt)
        {
            if (dt.ToString("dd/MM/yyyy").Equals("01/01/0001"))
            {
                return "";
            }
            else
            {
                return dt.ToString("dd/MM/yyyy");
            }
        }
        public void carregarDados(String nomeTransportadora, User usr)
        {
            String sql = "Select * from  Transportadora where Nome_transportadora='" + nomeTransportadora + "'";
            SqlDataReader rs = null;
            try
            {


                rs = Conexao.consulta(sql, usr, true);
                if (rs.Read())
                {
                    Nome_transportadora = rs["Nome_transportadora"].ToString();
                    Razao_social = rs["Razao_social"].ToString();
                    endereco = rs["endereco"].ToString();
                    cidade = rs["cidade"].ToString();
                    estado = rs["estado"].ToString();
                    cnpj = rs["cnpj"].ToString();
                    ie = rs["ie"].ToString();
                    Placa = rs["Placa"].ToString();
                    Telefones = rs["Telefones"].ToString();
                    padrao = rs["padrao"].ToString().Equals("1");
                }

            }
            catch (Exception)
            {

                throw;
            }
            finally
            {
                if (rs != null)
                    rs.Close();

            }
        }
        private void update()
        {
            try
            {
                String sql = "update  Transportadora set " +

                              "Razao_social='" + Razao_social + "'" +
                              ",endereco='" + endereco + "'" +
                              ",cidade='" + cidade + "'" +
                              ",estado='" + estado + "'" +
                              ",cnpj='" + cnpj + "'" +
                              ",ie='" + ie + "'" +
                              ",Placa='" + Placa + "'" +
                              ",Telefones='" + Telefones + "'" +
                              ",padrao ="+(padrao?"1":"0")+
                    "  where Nome_transportadora='" + Nome_transportadora + "'"
                        ;
                Conexao.executarSql(sql);
            }
            catch (Exception err)
            {
                throw new Exception("nao foi possivel Atualizar os valores erro:" + err.Message);
            }
        }
        public bool salvar(bool novo)
        {

            if(padrao)
            {
                int qtd = Funcoes.intTry( Conexao.retornaUmValor("Select count(*) from transportadora where Nome_transportadora <>'" + Nome_transportadora + "' and padrao = 1",null));
                if(qtd >0)
                {
                    Conexao.executarSql("update transportadora set padrao = 0 where Nome_transportadora <>'" + Nome_transportadora + "' and padrao = 1");
                }
            }

            if (novo)
            {
                insert();
            }
            else
            {
                update();
            }
            return true;
        }

        public bool excluir()
        {
            int nfs = int.Parse(Conexao.retornaUmValor("select COUNT(nome_transportadora) from nf where nome_transportadora = '" + Nome_transportadora + "'", null));
            if (nfs > 0)
            {
                throw new Exception("Não é possivel Excluir a Transportadora que já tenha sido relacioanda a Uma Nota Fiscal!");

            }
            else
            {
                String sql = "delete from Transportadora  where Nome_transportadora='" + Nome_transportadora + "'";
                Conexao.executarSql(sql);
            }
            return true;
        }

        private void insert()
        {
            try
            {
                String sql = " insert into Transportadora ( " +
                          "Nome_transportadora," +
                          "Razao_social," +
                          "endereco," +
                          "cidade," +
                          "estado," +
                          "cnpj," +
                          "ie," +
                          "Placa," +
                          "Telefones" +
                          ",padrao"+
                     " )values (" +
                          "'" + Nome_transportadora + "'" +
                          "," + "'" + Razao_social + "'" +
                          "," + "'" + endereco + "'" +
                          "," + "'" + cidade + "'" +
                          "," + "'" + estado + "'" +
                          "," + "'" + cnpj + "'" +
                          "," + "'" + ie + "'" +
                          "," + "'" + Placa + "'" +
                          "," + "'" + Telefones + "'" +
                          ","+(padrao?"1":"0")+
                         ");";

                Conexao.executarSql(sql);
            }
            catch (Exception err)
            {
                throw new Exception("nao foi possivel Inserir os valores erro:" + err.Message);
            }
        }
    }
