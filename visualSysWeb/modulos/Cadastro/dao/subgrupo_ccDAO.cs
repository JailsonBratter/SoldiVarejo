             String sql="Select * from  subgrupo_cc where codigo_subGrupo='"+ codSubGrupo+"' AND FILIAL='MATRIZ'";
             SqlDataReader rs = Conexao.consulta(sql, null,true);
             carregarDados(rs);
         }
 
          private String dataBr(DateTime dt) {
              if (dt.ToString("dd/MM/yyyy").Equals("01/01/0001")){
                  return "";
              }else{
                  return dt.ToString("dd/MM/yyyy");
              }
          }
          public void carregarDados(SqlDataReader rs){
              if(rs.Read()){
              Filial = rs["Filial"].ToString();
              Codigo_Subgrupo = rs["Codigo_Subgrupo"].ToString();
              codigo_grupo = rs["codigo_grupo"].ToString();
              Descricao_Subgrupo = rs["Descricao_Subgrupo"].ToString();
              }
              if(rs != null)
                  rs.Close();
          }
        private void update(){
        try{
         String sql= "update  subgrupo_cc set "+
                       "codigo_grupo='"+codigo_grupo+"'"+
                       ",Descricao_Subgrupo='"+Descricao_Subgrupo+"'"+
             "  where Filial='MATRIZ' and Codigo_Subgrupo='"+Codigo_Subgrupo+"'"
                       
                 ;
             Conexao.executarSql(sql);
             }catch (Exception err){
                 throw new Exception("nao foi possivel Atualizar os valores erro:"+err.Message );
             }
         }
         public bool salvar(bool novo) {
                 if (novo)
                 {
                      insert();
                  }
                  else
                  {
                      update();
                  }
                  return true;
         }
         
         public bool excluir() {

             int cont = Conexao.countSql("select codigo_SUBgrupo from CENTRO_CUSTO where codigo_subgrupo='" + Codigo_Subgrupo + "'", null);
             if (cont > 0)
             {
                 throw new Exception("NÃO É PERMITIDO EXCLUIR UM SUBGRUPO QUE CONTENHA CENTRO DE CUSTOS CADASTRADOS");
             }
             String sql = "delete from subgrupo_cc  where Filial='MATRIZ' and Codigo_Subgrupo='" + Codigo_Subgrupo + "'";
             Conexao.executarSql(sql);
             return true;
         }
         
