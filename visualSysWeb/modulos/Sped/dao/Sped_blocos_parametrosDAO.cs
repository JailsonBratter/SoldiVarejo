        public Sped_blocos_parametrosDAO(User usr) {
            this.usr = usr;

        }
        public Sped_blocos_parametrosDAO(int id_parametro, int id_bloco, User usr)
        {
            this.usr = usr;
            String sql = "Select * from  sped_blocos_parametros where id_parametro =" + id_parametro+" and id_bloco ="+id_bloco;
            SqlDataReader rs = Conexao.consulta(sql, usr, true);
            carregarDados(rs);
        }

        private String dataBr(DateTime dt)
        {
            if (dt.ToString("dd/MM/yyyy").Equals("01/01/0001"))
            {
                return "";
            }
            else
            {
                return dt.ToString("dd/MM/yyyy");
            }
        }
        public void carregarDados(SqlDataReader rs)
        {
            if (rs.Read())
            {
                id_parametro = (rs["id_parametro"] == null ? 0 : int.Parse(rs["id_parametro"].ToString()));
                id_bloco = (rs["id_bloco"] == null ? 0 : int.Parse(rs["id_bloco"].ToString()));
                nome_parametro = rs["nome_parametro"].ToString();
                tipo_dados = rs["tipo_dados"].ToString();
                campo_pai = rs["campo_pai"].ToString();
                herda_pai = rs["herda_pai"].ToString().Equals("1");
            }
            if (rs != null)
                rs.Close();
        }
        private void update(SqlConnection conn, SqlTransaction tran)
        {
            try
            {
                String sql = "update  sped_blocos_parametros set " +
                              "id_parametro=" + id_parametro +
                              ",id_bloco=" + id_bloco +
                              ",nome_parametro='" + nome_parametro + "'" +
                              ",tipo_dados='" + tipo_dados + "'" +
                              ",campo_pai='" + campo_pai + "'" +
                              ",herda_pai ="+(herda_pai?"1":"0")+
                    "  where id_parametro =" + id_parametro + " and id_bloco =" + id_bloco;
                        ;
                Conexao.executarSql(sql,conn,tran);
            }
            catch (Exception err)
            {
                throw new Exception("nao foi possivel Atualizar os valores erro:" + err.Message);
            }
        }
        public bool salvar(bool novo, SqlConnection conn, SqlTransaction tran)
        {
            if (novo)
            {
                insert(conn,tran);
            }
            else
            {
                update(conn,tran);
            }
            return true;
        }

        public bool excluir()
        {
            String sql = "delete from sped_blocos_parametros  where id_parametro =" + id_parametro + " and id_bloco =" + id_bloco;
            Conexao.executarSql(sql);
            return true;
        }

        private void insert(SqlConnection conn, SqlTransaction tran)
        {
            try
            {
                if(id_bloco==0)
                    int.TryParse(Conexao.retornaUmValor("Select isnull(MAX(ID),0)+1 from sped_blocos_parametros",null), out id_bloco);
                
                String sql = " insert into sped_blocos_parametros " +
                          "id_parametro," +
                          "id_bloco," +
                          "nome_parametro," +
                          "tipo_dados," +
                          "campo_pai," +
                          "herda_pai"+
                     " values " +
                          id_parametro +
                          "," + id_bloco +
                          "," + "'" + nome_parametro + "'" +
                          "," + "'" + tipo_dados + "'" +
                          "," + "'" + campo_pai + "'" +
                          ","+(herda_pai?"1":"0")+
                         ";";

                Conexao.executarSql(sql,conn,tran);
            }
            catch (Exception err)
            {
                throw new Exception("nao foi possivel Inserir os valores erro:" + err.Message);
            }
        }
